package math.random;

/**
 * Math.random()
 * This method returns a random double value between 0.0 (inclusive) and 1.0 (exclusive).
 */
public class Main {
    public static void main(String[] args) {
        System.out.println("Random number: " + Math.random()); // Random number between 0.0 and 1.0
    }
}
