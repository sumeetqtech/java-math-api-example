package math.abs;

/**
 * Math.abs()
 * This method returns the absolute value of a number.
 */
public class Main {
    public static void main(String[] args) {
        int num1 = -10;
        double num2 = 5.5;

        System.out.println("Absolute value of " + num1 + " is: " + Math.abs(num1)); // 10
        System.out.println("Absolute value of " + num2 + " is: " + Math.abs(num2)); // 5.5
    }
}
