package math.min.max;

/**
 * Math.min(), Math.max()
 * These methods return the smaller or larger of two values.
 */
public class Main {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;

        System.out.println("Minimum of " + a + " and " + b + " is: " + Math.min(a, b)); // 5
        System.out.println("Maximum of " + a + " and " + b + " is: " + Math.max(a, b)); // 10
    }
}
