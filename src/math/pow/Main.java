package math.pow;

/**
 * Math.pow()
 * This method raises a base to the power of an exponent.
 */
public class Main {
    public static void main(String[] args) {
        double base = 2.0;
        double exponent = 3.0;

        System.out.println(base + " raised to the power of " + exponent + " is: " + Math.pow(base, exponent)); // 8.0
    }
}